package Conditional;

public class IfConditions {
	
	// Java Program to demonstrate 
	// if-else statement
	  
	    public static void main(String[] args) {
	      
	        int n = 10;

	        if (n > 5) {
	            System.out.println("The number is greater than 5.");
	        } else {
	            System.out.println("The number is 5 or less.");
	        }
	    }

}
