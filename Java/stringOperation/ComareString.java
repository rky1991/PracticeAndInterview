package stringOperation;

public class ComareString {
	
	
	public static void main(String[] args) {
		 String st="4";
		 String tr="4";
		 
		 System.out.println("Adding Two Strings -->>"+st+tr);
		 
		 Double d = Double.valueOf(st);
		 Double d1 = Double.valueOf(tr);
		 
		 System.out.println("Converting TO Double --->>"+d+" "+d1);
		 System.out.println(d+d1);
		 
		 
		 int t=Integer.valueOf(st);
		 int t1=Integer.valueOf(tr);
		 int parse=Integer.parseInt(st);
		 
		 System.out.println("Converting TO integer --->>"+t+" "+t1);
		 System.out.println(t+t1+parse);
		 
		 
		 
		 
	
		 
	

	}

}
